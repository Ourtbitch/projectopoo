    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        if (!txtNombres.getText().isEmpty()&&
                !txtApellidos.getText().isEmpty()&&
                !txtCorreo.getText().isEmpty()&&
                !txtUsuario.getText().isEmpty()&&
                !txtContraseña.getText().isEmpty()){

            Usuario usuario= new Usuario(txtUsuario.getText(), txtContraseña.getText(), txtNombres.getText(), txtApellidos.getText(), txtCorreo.getText());
            
            if (UsuarioLogic.modificar(usuario)) {
                JOptionPane.showMessageDialog(this, "Usuario nodificado exitosamente");
            } else {
                JOptionPane.showMessageDialog(this, "Usuario no encontrado");
            }
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (!txtUsuario.getText().isEmpty()){

            Usuario usuario= new Usuario(txtUsuario.getText(), txtContraseña.getText(), txtNombres.getText(), txtApellidos.getText(), txtCorreo.getText());
            
            if (UsuarioLogic.eliminar(txtUsuario.getText())) {
                JOptionPane.showMessageDialog(this, "Usuario eliminado exitosamente");
            } else {
                JOptionPane.showMessageDialog(this, "Usuario no encontrado");
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

