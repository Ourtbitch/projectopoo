    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        if (!txtNombres.getText().isEmpty()&&!txtApellidos.getText().isEmpty()&&!txtCorreo.getText().isEmpty()&&!txtUsuario.getText().isEmpty()&&!txtContraseña.getText().isEmpty()){

            Usuario usuario= new Usuario(txtUsuario.getText(), txtContraseña.getText(), txtNombres.getText(), txtApellidos.getText(), txtCorreo.getText());
            
            if (UsuarioLogic.insertar(usuario)) {
                JOptionPane.showMessageDialog(this, "Usuario registrado exitosamente");
            } else {
                JOptionPane.showMessageDialog(this, "Usuario ya existente");
            }
        }else {
            JOptionPane.showMessageDialog(this, "No se agregó datos");
        }
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

